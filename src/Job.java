
public class Job {
private int id;
private long salary;


public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public long getSalary() {
	return salary;
}
public void setSalary(long salary) {
	this.salary = salary;
}
public String getRole() {
	return role;
}
public void setRole(String role) {
	this.role = role;
}
private String role;
}
